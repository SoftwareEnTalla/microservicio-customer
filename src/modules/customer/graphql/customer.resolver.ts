  @Mutation(() => CustomerResponse<Customer>)
  async createOrUpdateCustomer(
    @Args("data", { type: () => CreateOrUpdateCustomerDto })
    data: CreateOrUpdateCustomerDto
  ): Promise<CustomerResponse<Customer>> {
    if (data.id) {
      const existingCustomer = await this.service.findById(data.id);
      if (existingCustomer) {
        return this.commandBus.execute(
          new UpdateCustomerCommand(data, {
            instance: data,
            metadata: {
              initiatedBy:
                (data.input as CreateCustomerDto | UpdateCustomerDto).createdBy ||
                'system',
              correlationId: data.id,
            },
          })
        );
      }
    }
    return this.commandBus.execute(
      new CreateCustomerCommand(data, {
        instance: data,
        metadata: {
          initiatedBy:
            (data.input as CreateCustomerDto | UpdateCustomerDto).createdBy ||
            'system',
          correlationId: data.id || uuidv4(),
        },
      })
    );
  }


